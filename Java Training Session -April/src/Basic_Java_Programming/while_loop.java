package Basic_Java_Programming;

public class while_loop {

	public static void main(String[] args) {
		
		// while loop:
		/*
		  	syntax-
		 
			while(condition)
			{
			// body of stmt
			// Increment / decrement
			}
		*/
		
		int a =10;
		while(a >= 1) {
			System.out.println(a);
			a--;
		}
		

	}

}
