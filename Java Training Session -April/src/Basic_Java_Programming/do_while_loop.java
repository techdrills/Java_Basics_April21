package Basic_Java_Programming;

public class do_while_loop {

	public static void main(String[] args) {
		
		/* dowhile loop
		 * 
		 * syntax-
		 * 
		 * do{
		 * body of stmt
		 * } while(condition);
		 * 
		 */
		
		int a= 1;
		
		do {
			System.out.println(a);
			a++;
		}while(a <= 10);

	}

}
